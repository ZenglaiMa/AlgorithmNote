
// 题目: 输入一个三位数, 将其反转后输出.
// 样例输入: 127   520
// 样例输出: 721   25

#include <stdio.h>

int main(void) {

    int n, a, b, c;
    scanf("%d", &n);
    a = n / 100; // 百位
    b = n / 10 % 10; // 十位
    c = n % 10; // 个位
    printf("%d\n", c * 100 + b * 10 + a);
    //printf("%03d\n", c * 100 + b * 10 + a);

    return 0;
}

// note: 1. 百位、十位、个位的分离方法;
//       2. 如果输入520想得到025, 只需输出时格式设置为 %03d 即可.
